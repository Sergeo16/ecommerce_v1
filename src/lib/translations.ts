export type TranslationKey = keyof typeof translations.fr;

/** Convertit un statut livraison (ex: COURIER_ACCEPTED) en clé de traduction (ex: deliveryStatusCourierAccepted). */
export function deliveryStatusToKey(status: string): TranslationKey {
  const camel = status.toLowerCase().replace(/_([a-z])/g, (_, c) => c.toUpperCase());
  const key = `deliveryStatus${camel.charAt(0).toUpperCase()}${camel.slice(1)}` as TranslationKey;
  return key in translations.fr ? key : ('deliveryStatusPending' as TranslationKey);
}

/** Convertit un statut commande (ex: DELIVERED) en clé de traduction (ex: orderStatusDelivered). */
export function orderStatusToKey(status: string): TranslationKey {
  const camel = status.charAt(0).toUpperCase() + status.slice(1).toLowerCase();
  const key = `orderStatus${camel}` as TranslationKey;
  return key in translations.fr ? key : ('orderStatusPending' as TranslationKey);
}
